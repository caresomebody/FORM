package com.phoenix.form;

public class hotel {
    private int gambar;
    private String namaHotel;
    private String kebutuhanIkan;

    public int getGambar() {
        return gambar;
    }

    public void setGambar(int gambar) {
        this.gambar = gambar;
    }

    public String getNamaHotel() {
        return namaHotel;
    }

    public void setNamaHotel(String namaHotel) {
        this.namaHotel = namaHotel;
    }

    public String getKebutuhanIkan() {
        return kebutuhanIkan;
    }

    public void setKebutuhanIkan(String kebutuhanIkan) {
        this.kebutuhanIkan = kebutuhanIkan;
    }
}
